class Plot:
	
	histName = "none"
	plotName = "none"
	xaxis   = "none"
	yaxis	= "none"
	cut = ""
	variable = ""
	xMin = 0
	xMax = 0
	projLow = 0
	projHigh = 0
	nBins = 0
	binning = []
	yMin 	= 0
	yMax	= 0 
	rebin = 1
	fileName = "none.pdf"
	log = False
	logX = False
	useJets = False
	plot2D = False
	
	def __init__(self,histName,plotName, yRange = None, xRange = None, projRange = None, nBins = 0, xLabel = "", yLabel = "",log=False,logX=False,rebin = None, binning = [], useJets=False,plot2D=False,muon=True):
		self.histName=histName
		self.xaxis=xLabel
		self.yaxis=yLabel
		self.nBins = nBins
		self.binning = binning
		self.xMin= None
		self.xMax= None
		self.yMin= None
		self.yMax= None
		self.plotName = plotName
		self.fileName= plotName
		self.useJets = useJets
		self.plot2D = plot2D
		self.muon=muon
		if rebin != None:
			self.rebin = rebin
		if log:
			self.fileName+"_log" 
		self.log = log
		self.logX = logX

		if yRange != None:
			self.yMin = yRange[0]
			self.yMax = yRange[1]
		if xRange != None:
			self.xMin = xRange[0]
			self.xMax = xRange[1]
		if projRange != None:
			self.projLow = projRange[0]
			self.projHigh = projRange[1]



